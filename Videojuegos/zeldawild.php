     <H1> DESCRIPCION:</H1>
     <H4><H4>- Abre los ojos<BR> 
<BR>Sin reino. Sin memoria. Tras un sueño de 100 años, Link despierta en un mundo que no es capaz de recordar. Nuestro héroe legendario deberá explorar un mundo vasto y peligroso, y recuperar su memoria antes de que Hyrule desaparezca para siempre. <BR> 

<BR>Link emprende un viaje para encontrar respuestas en el que solo dispondrá de aquello que encuentre por el camino.<br>

<BR>- La piedra sheikah<BR> 
<BR>Una misteriosa piedra cuyo centro emite una luz brillante. Link no recuerda haber visto nada semejante antes y, sin embargo, le resulta familiar...<BR> 

<BR>- Explora Hyrule<BR>
<BR>La piedra sheikah contiene un mapa de Hyrule que guiará a Link en su viaje. También tiene un catalejo que permite identificar lugares de interés en la distancia<BR> 

<BR>- Descargar módulos <BR>
Link puede descargar módulos en ciertos santuarios para aprender nuevas habilidades con las que resolver puzles y desbloquear nuevas áreas. <BR>

<BR>- Organízate <BR>
<BR>- Link puede planificar su aventura con la piedra sheikah, organizar sus objetos en la alforja y llevar cuenta de las misiones en el diario de viaje. <BR>-  

<BR>- - Descubre el enorme mundo que te rodea <BR>-
<BR>- Explora los parajes de Hyrule como te plazca. Escala torres y montañas en busca de nuevos destinos y sigue tu propio camino para llegar hasta ellos. <BR>-  

<BR>- - Torres<BR>-  
<BR>- Antes de aventurarse hacia nuevos territorios, Link puede trepar a lo alto de una torre para otear el horizonte.<BR>-  

<BR>- - Santuarios<BR>-  
<BR>- Hay más de 100 santuarios repartidos por todo Hyrule. Link deberá resolver los puzles que albergan para probar su heroísmo y conseguir recompensas.<BR>-  


<BR>- - Pueblos<BR>-  
<BR>- Link visitará muchos pueblos distintos durante su aventura, cada uno con su encanto único.<BR>-  

<BR>- - Establos<BR>-  
<BR>- Los establos ya no son solo para los caballos. Link también puede descansar para recuperar su salud y preparar comida o elixires antes de su siguiente aventura. <BR>-  

<BR>- - Por tierra, mar y aire<BR>-  
<BR>- Con tanto terreno por explorar, Link necesitará ayuda para desplazarse. Afortunadamente, Link contará con multitud de opciones de transporte. Podrá cubrir grandes distancias a caballo, navegar sobre las aguas de Hyrule en balsa o deslizarse montaña abajo sobre su escudo. Y por si esto fuera poco, el transporte aéreo también está al alcance de su mano gracias a la paravela.<BR>-

<BR>- - Lucha por tu reino 
Cuando Link despierta, se encuentra desarmado e indefenso. Pero Link tiene ingenio y recursos, así que podrá adquirir un montón de armas diferentes para usar en combate. Estas armas, sin embargo, se irán desgastando con el paso del tiempo, así que no deberá perder ocasión de renovar su arsenal. <BR>-  

<BR>- - Caza y recolección<BR>-
 <BR>-¡Link no puede salvar Hyrule con el estómago vacío! Caza animales salvajes, recoge frutas y vegetales o saquea los campamentos enemigos en busca de comida. ¡También aprenderás nuevas recetas para darles un toque de sabor a tus ingredientes! Estas innovaciones culinarias no solo son más sabrosas, sino que también servirán para curar a Link y mejorar sus estadísticas.<BR>-  

<BR>- - Vestido para matar<BR>-  
<BR>- Link lleva algunos conjuntos ciertamente elegantes, pero algunos tienen una función añadida, como protegerlo de los elementos o desviar los ataques enemigos. Encontrarás todo tipo de ropas para Link, desde prendas para la nieve hasta armaduras, pero ten en cuenta un detalle, en caso de tormenta, llevar un atuendo metálico puede no ser la mejor de las ideas... <BR>
